package Java.Bit_Manipulation;

public class _Bit_manipulation_Subsets {
    public static void main(String[] args) {
        // Now as we shift left the number increases with a power of 2^n
        int a = 1;

        // left shift by 1 = 2
        System.out.println(a<<1);

        // left shift by 2 = 4
        System.out.println(a<<2);

        // left shift by 3 = 8
        System.out.println(a<<3);

        // left shift by 4 = 16
        System.out.println(a<<4);
    }
}
